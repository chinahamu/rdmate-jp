#!/usr/bin/env node

import { existsSync, readFileSync, readdirSync, statSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const repoRoot = path.resolve(path.dirname(__filename), '..');
const nodeModulesDir = path.join(repoRoot, 'node_modules');
const outputPath = path.join(repoRoot, 'THIRD_PARTY_LICENSES.md');

const packageJson = JSON.parse(readFileSync(path.join(repoRoot, 'package.json'), 'utf8'));
const declaredDependencies = new Map([
  ...Object.keys(packageJson.dependencies ?? {}).map((name) => [name, 'dependency']),
  ...Object.keys(packageJson.devDependencies ?? {}).map((name) => [name, 'devDependency']),
]);

function readJson(filePath) {
  try {
    return JSON.parse(readFileSync(filePath, 'utf8'));
  } catch {
    return null;
  }
}

function normalizeRepository(repository) {
  if (!repository) return '';
  if (typeof repository === 'string') return repository;
  return repository.url ?? '';
}

function normalizeLicense(pkg) {
  if (typeof pkg.license === 'string') return pkg.license;
  if (Array.isArray(pkg.licenses)) {
    return pkg.licenses
      .map((license) => (typeof license === 'string' ? license : license.type))
      .filter(Boolean)
      .join(', ');
  }
  return 'UNKNOWN';
}

function collectDirectPackageJsonPaths() {
  const paths = [];

  for (const packageName of declaredDependencies.keys()) {
    const packagePath = path.join(nodeModulesDir, packageName, 'package.json');
    if (existsSync(packagePath)) {
      paths.push(packagePath);
    }
  }

  return paths;
}

function collectPnpmPackageJsonPaths() {
  const pnpmDir = path.join(nodeModulesDir, '.pnpm');
  if (!existsSync(pnpmDir)) return [];

  const packageJsonPaths = [];
  const stack = [pnpmDir];

  while (stack.length > 0) {
    const current = stack.pop();
    if (!current) continue;

    for (const entry of readdirSync(current)) {
      if (entry === '.bin') continue;
      const entryPath = path.join(current, entry);
      const stats = statSync(entryPath);
      if (stats.isDirectory()) {
        stack.push(entryPath);
      } else if (entry === 'package.json' && entryPath.includes(`${path.sep}node_modules${path.sep}`)) {
        packageJsonPaths.push(entryPath);
      }
    }
  }

  return packageJsonPaths;
}

function collectPackages() {
  if (!existsSync(nodeModulesDir)) {
    console.error('node_modules が見つかりません。先に `pnpm install` を実行してください。');
    process.exit(1);
  }

  const packageJsonPaths = [...collectDirectPackageJsonPaths(), ...collectPnpmPackageJsonPaths()];
  const packages = new Map();

  for (const packageJsonPath of packageJsonPaths) {
    const pkg = readJson(packageJsonPath);
    if (!pkg?.name || !pkg?.version) continue;

    const key = `${pkg.name}@${pkg.version}`;
    if (packages.has(key)) continue;

    packages.set(key, {
      name: pkg.name,
      version: pkg.version,
      license: normalizeLicense(pkg),
      repository: normalizeRepository(pkg.repository),
      homepage: pkg.homepage ?? '',
      declaredAs: declaredDependencies.get(pkg.name) ?? 'transitive',
    });
  }

  return [...packages.values()].sort((a, b) => `${a.name}@${a.version}`.localeCompare(`${b.name}@${b.version}`));
}

function renderMarkdown(packages) {
  const generatedAt = new Date().toISOString();
  const rows = packages.map((pkg) => {
    const repository = pkg.repository || pkg.homepage || '';
    return `| \`${pkg.name}\` | \`${pkg.version}\` | ${pkg.license} | ${pkg.declaredAs} | ${repository} |`;
  });

  return [
    '# Third-party licenses',
    '',
    'This file is generated by `pnpm licenses:generate`.',
    '',
    `Generated at: ${generatedAt}`,
    '',
    'RDMate JP itself is licensed under the MIT License. The dependencies listed below are distributed under their own licenses.',
    '',
    '| Package | Version | License | Type | Repository / Homepage |',
    '| --- | --- | --- | --- | --- |',
    ...rows,
    '',
    '## Notes',
    '',
    '- Run this generator after `pnpm install` so that package metadata is available under `node_modules`.',
    '- Review entries with `UNKNOWN` before publishing a release.',
    '- This generated file is a convenience summary and does not replace the license text distributed with each dependency.',
    '',
  ].join('\n');
}

const packages = collectPackages();
writeFileSync(outputPath, renderMarkdown(packages));
console.log(`Wrote ${path.relative(repoRoot, outputPath)} with ${packages.length} package entries.`);
